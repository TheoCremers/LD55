
public enum AuraEffect
{
    Healing, // heal x hp per tick
    Degen, // lose x hp per tick
    Frenzy, // decreased attack cooldown, increased movement speed
    Slow, // increased attack cooldown, reduced movement speed
    Empower, // increased attack damage
    Weaken, // decreased attack damage
    Protect, // decreased damage taken
    Expose // increased damage taken
    
}
